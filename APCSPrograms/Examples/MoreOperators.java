public  class MoreOperators	{
	public static void MoreOperators(String[] args)	{
	
		int n1 = 5;
		int n2 = 7;
		int n3 = n1++ * n2--;	// ++ = add 1, -- = subtract 1
		System.out.println(n3);
		
		n1 +=8;					//n1 = n1 + 8
		System.out.println(n1);
		
		n2 *= 3;				//n2 = n2 * 3
		System.out.println(n2);
		
		//you can do the += with Strings as well, It's Totally LEGAL!!??
		
		
		
	}
}